a = 10
b = 20

# For Addition
print(a + b)
# For Subtraction
print(a - b)
# For Multiplication
print(a * b)
# For Division
print(a / b)
# For Modules(i.e remainder)
print(a % b)
# For Exponential(i.e 10^20)
print(a ** b)
# For Floor division(i.e Dividing one number by another and then rounding the result to the closest integer that is smaller)
print(a // b)

# Numeric types
"""int"""
x = 5.5
x = int(x)
print(x)
"""float"""
y = 10
y = float(y)
print(y)
"""str"""
z = 20
z = str(z)
print(z)

# To check the Numeric Type
print(type(x))
print(type(y))
print(type(z))

